export const exportToPDF = (data: SEOReportData) => {
  const html = generateSEOReportHTML(data);
  const blob = new Blob([html], { type: 'text/html' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `seo-report-${Date.now()}.html`;
  a.click();
  URL.revokeObjectURL(url);
};

export const exportToCSV = (data: SEOReportData) => {
  const csv = `
"SEO Analysis Report"
"Generated","${data.timestamp}"
""
"Title","${data.title.replace(/"/g, '""')}"
"Description","${data.description.replace(/"/g, '""')}"
"Score","${data.score}"
"Grade","${data.grade}"
""
"Tags"
${data.tags.map(tag => `"${tag}"`).join('\n')}
""
"Hashtags"
${data.hashtags.map(tag => `"${tag}"`).join('\n')}
""
"Recommendations"
${data.recommendations.map(rec => `"${rec.replace(/"/g, '""')}"`).join('\n')}
  `.trim();

  const blob = new Blob([csv], { type: 'text/csv' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `seo-report-${Date.now()}.csv`;
  a.click();
  URL.revokeObjectURL(url);
};
